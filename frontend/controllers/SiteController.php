    /**
     * Displays homepage.
     *
     * @return mixed
     */
    public function actionIndex()
    {
        $userType = Yii::$app->user->identity->type;

        if ($userType == "0") {
            return $this->redirect(['../lecturer/dashboard']);
        } else {
            return $this->redirect(['../student/dashboard']);
        }
    }

    /**
     * Displays homepage.
